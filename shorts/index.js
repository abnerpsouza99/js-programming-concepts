
const numero = 11;
let mensagem = '';

// If convencional
if (numero > 10) {
    mensagem = `Número é maior que 10`;
} else {
    mensagem = `Número não é maior que 10`;
}

console.log(mensagem);

// If ternário
mensagem = numero > 10 ? `Número é maior que 10`
    : `Número não é maior que 10`;

console.log(mensagem);






